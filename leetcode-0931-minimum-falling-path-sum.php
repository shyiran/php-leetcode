    function main($matrix)
    {
        $n = count($matrix);
        $dp = [];
        for ($i = 0; $i < $n; $i++) {
            for ($j = 0; $j < $n; $j++) {
                if ($i == 0) {
                    // 第一行
                    $dp[0][$j] = $matrix[0][$j];
                } else {
                    // 上一行的mid
                    $arr = [$dp[$i - 1][$j]];
                    // 存在上一行的left的话
                    if (isset($dp[$i - 1][$j - 1])) {
                        $arr[] = $dp[$i - 1][$j - 1];
                    }
                    // 存在上一行的right的话
                    if (isset($dp[$i - 1][$j + 1])) {
                        $arr[] = $dp[$i - 1][$j + 1];
                    }
                    $dp[$i][$j] = min($arr) + $matrix[$i][$j];
                }
            }
        }
        return min($dp[$n-1]);
    }

